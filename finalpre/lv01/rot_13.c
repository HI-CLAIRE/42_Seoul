#include <unistd.h>

char	rot_13(char c)
{
	if ((c >= 'A' && c <= 'M') || (c >= 'a' && c <= 'm'))
		c += 13;
	else if ((c >= 'N' && c <= 'Z') || (c >= 'n' && c <= 'z'))
		c -= 13;
	return (c);
}

int main(int ac, char **av)
{
	char c;
	int	i = 0;

	if (ac == 2)
	{

		while (av[1][i])
		{
			c = rot_13(av[1][i]);
			write(1, &c, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
