int	ft_plus(int x, int y)
{
	return (x + y);
}

int	ft_minus(int x, int y)
{
	return (x - y);
}

int	ft_multiple(int x, int y)
{
	return (x * y);
}

int	ft_division(int x, int y)
{
	return (x / y);
}

int	ft_modulo(int x, int y)
{
	return (x % y);
}
